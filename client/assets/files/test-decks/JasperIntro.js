  <p data-view-type="course">CALIBRATION: With computer analysis of digital images taken at the microscope, the images are represented in the form of
    picture elements, or pixels, which are small dots making up the image. In order to perform measurements, the image analysis
    tools need to know how many pixels there are in one unit length of the image. The unit length may be in millimeters, micrometers,
    or any other unit chosen according to the overall size of the image and its important components. The process of assigning
    the number of pixels per unit length to an image is known as calibration. Before undertaking any analysis, a system setup
    must be calibrated so that measurements such as length, perimeter, or area are calculated in a specific unit, rather than
    as number of pixels measured (Reedy and Kamboj 2004). The calibration for each setup depends on the magnification of the
    objective lens that is used to view the image under the microscope, the resolution under which the image is stored, and
    the digital camera/microscope combination that is being used to acquire the image. This calibration measure will then
    apply to all images taken with that combination. The appropriate calibration measure must be imported and applied to each
    image at the time of analysis, so it is crucial to store that information on the image itself, in the filename, or in
    an image database. Most digital microscope cameras include the capability of marking each image directly at the time of
    image capture with a scale bar. Where this was not done, a scale bar can be inserted after the fact with most image analysis
    software packages. It is important to include the scale bar in published reports rather than listing a magnification.
    Calculated magnifications apply to what the eye sees when looking into the microscope lens. In traditional work, magnifications
    had to be recalculated to adjust for publication size of photomicrographs. In image analysis work there are also many
    intermediate steps in which the digital image may be resized, which also would affect magnification. The original image
    as viewed under the microscope can be thought of as a rubber band being extended or contracted during photography, analysis,
    and final publication, making magnifications impossible to track. Thus most journals require scale bars in thin-section
    images, but do not list magnifications.</p>
